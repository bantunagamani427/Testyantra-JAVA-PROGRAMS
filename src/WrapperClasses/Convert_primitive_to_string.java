package WrapperClasses;

public class Convert_primitive_to_string {

	public static void main(String[] args) {
		int a=20;
		String str = String.valueOf(a);
		System.out.println(str+"kl");

		double d=45.6;
		String str1=String.valueOf(d);
		System.out.println(str1+"dsf");

		boolean b=true;
		String str2=String.valueOf(b);
		System.out.println(str2+"r");
	}

}
