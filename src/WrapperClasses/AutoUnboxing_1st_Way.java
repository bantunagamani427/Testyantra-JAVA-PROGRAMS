package WrapperClasses;

public class AutoUnboxing_1st_Way {

	public static void main(String[] args) {
		Integer i=20;
		int i1=i;
		System.out.println(i1);

		Double d=90.8;
		double d1=d;
		System.out.println(d1);

		Float f=65.7f;
		float f1=f;
		System.out.println(f1);
	}

}
